package javabasicconcepts;

public class Unicode {
    public static void main(String[] args) {
        char myChar='D';
        char myUnicodeChar='\u0044';
        System.out.println("My first char is "+myChar);
        System.out.println("My first unicode char is "+myUnicodeChar);
        char mySecondChar='F';
        char mySecondUnicodeChar='\u0046';
        System.out.println("My second char is "+mySecondChar);
        System.out.println("My second unicode char is "+mySecondUnicodeChar);
        char myThirdChar='t';
        char myThirdUnicodeChar='\u0074';
        System.out.println("My third char is "+mySecondChar);
        System.out.println("My third unicode char is "+mySecondUnicodeChar);

    }
}
